package Loops;

public class ForLoops {
    public static void main(String[] args){
        System.out.println("For döngüsü");
        for(int i = 1 ; i <=5 ; i++){
            System.out.println(i);
        }

        int j = 1;
        System.out.println("While döngüsü");
        while(j<=5){
            System.out.println(j);
            j++;
        }
    }
}
